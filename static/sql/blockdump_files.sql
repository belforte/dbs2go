SELECT
    F.CHECK_SUM,
    F.ADLER32,
    F.FILE_SIZE,
    F.EVENT_COUNT,
    FT.FILE_TYPE,
    BH.BRANCH_HASH,
    F.LAST_MODIFIED_BY,
    F.LOGICAL_FILE_NAME,
    F.MD5,
    F.AUTO_CROSS_SECTION
FROM {{.Owner}}.FILES F
JOIN {{.Owner}}.BLOCKS B ON B.BLOCK_ID = F.BLOCK_ID
JOIN {{.Owner}}.FILE_DATA_TYPES FT ON F.FILE_TYPE_ID = FT.FILE_TYPE_ID
JOIN {{.Owner}}.BRANCH_HASHES BH ON F.BRANCH_HASH_ID = BH.BRANCH_HASH_ID
WHERE B.BLOCK_NAME = :blk
